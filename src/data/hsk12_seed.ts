type Raw = Array<[hanzi: string, pinyin: string, english: string]>;

function uniqByHanzi(list: Raw): Raw {
  const seen = new Set<string>();
  const out: Raw = [];
  for (const item of list) {
    const key = item[0];
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(item);
  }
  return out;
}

